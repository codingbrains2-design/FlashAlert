    <h2 class="Headline"><strong>Guidelines for posting Emergency Messages on FlashAlert</strong></h2>
    <p>While it is important to convey what you need to say, following some basic guidelines will: <br>
        <strong>A)</strong> mimimize the chance that the media will shorten your message and it may lose crucial
        content; <br>
        <strong>B)</strong> not confuse the public by one org showing detailed information while others do not. Thus:
    </p>
    <p><strong>1.</strong>Use the drop-down <strong>Quick Report</strong> menus. They are concise and spelled correctly.
        If your message applies to tomorrow, simply use the menus and the "For tomorrow" checkbox. FlashAlert is working
        with TV stations to enable the direct import of your messages to their crawl ("ticker") systems, which could get
        your message on screen is as little as five minutes. However, this will only work for menu-based messages,
        because a station editor will have to review/shorten custom messages. (Note: information about TV crawls is not
        applicable to basic tier users in Portland and Colorado Springs because their information goes only to station
        websites.)</p>
    <p> <strong>2.</strong> If you must use the Custom Report field, remember that: <strong><br>
            A)</strong> Your org name already leads off the message.<strong> <br>
            B</strong>) There is no need to include the day (and especialy the date/year!) since current day is always
        assumed and if it is for tomorrow, you can use the "For tomorrow" checkbox. <br>
        <strong>C)</strong> Cut the message to the bone. Complete sentences not required! Remember that some people will
        see your message as a text (truncating at 150 characters including header info) or Tweets (140 characters).
    </p>
    <p><strong>3.</strong> Do not state why you are closing if it is obvious, i.e. no "Due to the inclement weather..."
        Likewise, do not tell people to stay tuned to the media for updates. The stations will not run these messages,
        and it may coerce others into making the same statement and clogging up the web pages.</p>
    <p><strong>4.</strong> <strong>"Closed" means closed.</strong> Please inform your publics that closed means nothing
        is happening. Not "Closed, no school board meeting, no football practice, no choir concert..." The media will
        chop longer messages. Closed means closed, so say we all. </p>
    <p><strong>5.</strong> There's no need to include contact information if your reason for closing is the same as
        everyone else. It just makes the emails to the media longer.</p>
    <p><strong>6.</strong> By agreement of the participating superintendents a few years ago, FlashAlert users do not
        post "Open" messages. "Open" messages opens Pandora's box in terms of what days must you post such a message?
        However, if you have a situation where, for example, every other school district in your region is changing
        schedule but you are operating as usual, then posting "Normal schedule" may be in order.<br>
        <br>
        Tip: If you use the FlashAlert <em>Messenger</em> service to send information to parents/staff, you may send a
        "Normal schedule" message - <em>not checkboxing any of the cities for the media.</em>
    </p>
    <p><strong>7.</strong> When you choose newsroom cities to send to, PLEASE be picky. The ones marked in black do not
        have broadcast stations and thus you probably can skip them if it's a snow closure. If you are a college, please
        do not check every city just because you think you have a student living there. Students will get your closure
        from big-city media, not the hometown newspaper.</p>
    <p><strong>8.</strong> If 45 minutes have passed since posting and you see a TV station not scrolling your message
        or has it wrong, you may return to the posting page and trigger a reminder message directly to <em>that
            particular station.</em></p>
    <p><strong>9.</strong> One final tip: here is the shortcut to the login page: www.falogin.net (a direct path and no
        frames on the page).</p>
